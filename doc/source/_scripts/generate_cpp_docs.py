    def _is_private_class(self, class_name: str) -> bool:
        """Determine if a class is a private implementation class that should be excluded."""
        class_simple_name = class_name.split('::')[-1]
        
        # Check against excluded patterns from config
        if self._matches_excluded_pattern(class_name):
            return True
        if self._matches_excluded_pattern(class_simple_name):
            return True
        
        # Filter out private implementation classes
        private_patterns = [
            'Impl_',     # Implementation classes ending with Impl_
            '_Impl',     # Implementation classes ending with _Impl
            '_Internal', # Internal classes
            '_impl',     # lowercase impl
            'impl_',     # lowercase impl prefix
        ]
        
        # Check if class name ends with or contains private patterns
        for pattern in private_patterns:
            if class_simple_name.endswith(pattern) or pattern in class_simple_name:
                return True
        
        # Filter out classes with leading underscores (typically private)
        if class_simple_name.startswith('_'):
            return True
            
        # Filter out classes ending with underscore (like Impl_)
        if class_simple_name.endswith('_'):
            return True
            
        return False
    
