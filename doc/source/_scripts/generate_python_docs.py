def main():
    """Main function with command line argument support."""
    parser = argparse.ArgumentParser(description='Generate Python API documentation using pydoc-markdown')
    parser.add_argument('--clean-only', action='store_true', 
                       help='Only clean previously generated files, do not generate new documentation')
    parser.add_argument('--no-clean', action='store_true',
                       help='Skip cleaning step, only generate new documentation')
    
    args = parser.parse_args()
    
    if args.clean_only:
        # Only perform cleanup
        scripts_dir = Path(__file__).parent
        output_dir = scripts_dir.parent / 'reference' / 'python_api'
        if output_dir.exists():
            clean_generated_docs(output_dir, scripts_dir)
        else:
            print(f"📁 Output directory {output_dir} does not exist")
        print("🎉 Cleanup completed!")
    else:
        # Generate docs (with or without cleaning)
        if args.no_clean:
            generate_docs_no_clean()
        else:
            generate_docs()

